#include "libft.h"

int	findlen(int i)
{
	int	count;

	count = 0;
	while (i >= 10)
	{
		count++;
		i /= 10;
	}
}

char	*ft_itoa(int n)
{
	int	len;

	len = findlen(n);
	

}