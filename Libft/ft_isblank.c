#include "libft.h"

size_t ft_isblank(size_t c)
{
    if ((char)c == ' ' || (char)c == '\t' || (char)c == '\n' 
        || (char)c == '\v' || (char)c == '\f' || (char)c == '\r'
        || (char)c == '\a')
        return(1);
    return(0);
}
